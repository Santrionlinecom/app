#!/usr/bin/env node

// Usage:
// LICENSE_KEY_HASH_SECRET=your-secret node scripts/create-license-key.mjs
// LICENSE_KEY_HASH_SECRET=your-secret node scripts/create-license-key.mjs --expires-at=2027-12-31 --remote

import { execFileSync } from 'node:child_process';
import { randomBytes, randomUUID, webcrypto } from 'node:crypto';

const PRODUCT_SLUG = 'santri-cleaner-pro';
const PLAN = 'pro';
const MAX_DEVICES = 1;
const MIN_REASONABLE_MS = 1_000_000_000_000;
const KEY_ALPHABET = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
const SANTRI_CLEANER_PRO_FEATURES = [
	'deep_scan',
	'developer_cleaner',
	'creator_cleaner',
	'export_pdf',
	'ai_assistant'
];
const textEncoder = new TextEncoder();

const fail = (message) => {
	console.error(message);
	process.exit(1);
};

const sqlString = (value) => `'${String(value).replaceAll("'", "''")}'`;

const normalizeLicenseKeyInput = (value) => value.trim().toUpperCase();

const bytesToHex = (buffer) =>
	Array.from(new Uint8Array(buffer))
		.map((byte) => byte.toString(16).padStart(2, '0'))
		.join('');

const hashLicenseKey = async (licenseKey, secret) => {
	const key = await webcrypto.subtle.importKey(
		'raw',
		textEncoder.encode(secret.trim()),
		{ name: 'HMAC', hash: 'SHA-256' },
		false,
		['sign']
	);

	return bytesToHex(
		await webcrypto.subtle.sign('HMAC', key, textEncoder.encode(normalizeLicenseKeyInput(licenseKey)))
	);
};

const randomKeySegment = () => {
	const bytes = randomBytes(4);
	let segment = '';
	for (const byte of bytes) {
		segment += KEY_ALPHABET[byte % KEY_ALPHABET.length];
	}
	return segment;
};

const generateSantriCleanerProKey = () =>
	`SC-PRO-${randomKeySegment()}-${randomKeySegment()}-${randomKeySegment()}`;

const parseExpiresAt = (value) => {
	if (!value) return null;

	const trimmed = value.trim();
	if (!trimmed) return null;

	let parsed = Number(trimmed);
	if (!Number.isFinite(parsed)) {
		parsed = Date.parse(trimmed);
	}
	if (!Number.isFinite(parsed) || parsed <= 0) {
		fail('expiresAt tidak valid. Gunakan ISO date, ISO datetime, epoch seconds, atau epoch milliseconds.');
	}
	if (parsed < MIN_REASONABLE_MS) {
		parsed *= 1000;
	}

	return Math.floor(parsed);
};

const findChanges = (wranglerOutput) => {
	let parsed;
	try {
		parsed = JSON.parse(wranglerOutput);
	} catch {
		fail('Gagal membaca output wrangler.');
	}

	for (const statement of parsed) {
		const first = statement?.results?.[0];
		if (first && typeof first.changes === 'number') {
			return first.changes;
		}
	}

	fail('Gagal memastikan license tersimpan.');
};

const args = process.argv.slice(2);
const expiresAtArg = args.find((arg) => arg.startsWith('--expires-at='));
const secret = process.env.LICENSE_KEY_HASH_SECRET?.trim();

if (!secret) {
	fail('LICENSE_KEY_HASH_SECRET wajib tersedia di environment.');
}

const useRemote = args.includes('--remote');

if (args.some((arg) => arg !== '--remote' && !arg.startsWith('--expires-at='))) {
	fail('Argumen tidak dikenal. Gunakan --expires-at=YYYY-MM-DD dan/atau --remote.');
}

const licenseKey = generateSantriCleanerProKey();
const licenseKeyHash = await hashLicenseKey(licenseKey, secret);
const now = Date.now();
const expiresAt = parseExpiresAt(expiresAtArg?.slice('--expires-at='.length));
const internalLicenseId = `lic_${randomUUID()}`;
const featuresJson = JSON.stringify(SANTRI_CLEANER_PRO_FEATURES);

const command = `
INSERT INTO licenses (
	license_key,
	user_id,
	user_email,
	plan,
	status,
	device_limit,
	created_at,
	expires_at,
	notes,
	product_id,
	license_key_hash,
	max_devices,
	features_json,
	activated_at,
	updated_at
)
SELECT
	${sqlString(internalLicenseId)},
	NULL,
	NULL,
	${sqlString(PLAN)},
	'active',
	${MAX_DEVICES},
	${now},
	${expiresAt === null ? 'NULL' : expiresAt},
	'Generated by scripts/create-license-key.mjs',
	id,
	${sqlString(licenseKeyHash)},
	${MAX_DEVICES},
	${sqlString(featuresJson)},
	NULL,
	${now}
FROM products
WHERE slug = ${sqlString(PRODUCT_SLUG)} AND status = 'active';
SELECT changes() AS changes;
`;

let output;
try {
	output = execFileSync(
		process.platform === 'win32' ? 'npx.cmd' : 'npx',
		['wrangler', 'd1', 'execute', 'DB', useRemote ? '--remote' : '--local', '--command', command, '--json'],
		{ encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] }
	);
} catch (error) {
	const stderr = typeof error.stderr === 'string' ? error.stderr.trim() : '';
	fail(stderr || 'Gagal menyimpan license ke D1.');
}

const changes = findChanges(output);
if (changes !== 1) {
	fail(`Product ${PRODUCT_SLUG} tidak ditemukan atau tidak active. Pastikan migration 0046 sudah diterapkan.`);
}

process.stdout.write(`${licenseKey}\n`);
