		<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">2. Detox dari Toxic Digital Culture</h2>
		<p>Media sosial sering bikin kita insecure, FOMO, atau bahkan depresi karena terus-terusan compare diri sama orang lain. Di pesantren, kamu punya kesempatan untuk digital detox yang sehat. Bukan berarti anti-teknologi, tapi lebih ke mengatur prioritas. Kamu belajar untuk nggak jadi budak notifikasi dan lebih fokus pada pengembangan diri yang hakiki. Trust me, mental health kamu bakal jauh lebih baik!</p>

