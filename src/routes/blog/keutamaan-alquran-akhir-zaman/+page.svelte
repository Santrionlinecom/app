			<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">Praktik Amaliyah</h2>
			<ol class="space-y-3">
				<li><strong>Tilawah harian</strong>: minimal 1-2 halaman, ditingkatkan menjadi 1 juz ketika mampu.</li>
				<li><strong>Hafalan & murajaah</strong>: konsisten mengulang hafalan lama sebelum menambah hafalan baru.</li>
				<li><strong>Tadabbur</strong>: pilih 1-2 ayat harian untuk ditulis makna dan hikmahnya (catat di jurnal atau aplikasi catatan).</li>
