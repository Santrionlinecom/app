			<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">1. Sang Arsitek Niat: Fatima al-Fihri</h2>
			<p>
				Fatima binti Muhammad Al-Fihri berasal dari keluarga saudagar kaya di Qayrawan (Tunisia) yang hijrah ke Fez. Ketika ayah dan suaminya wafat, ia mewarisi harta besarâ€”namun niatnya jelas: menjadikannya amal jariyah yang terus mengalir.
			</p>

			<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">2. Tanah Kebun dan Mata Air (Awal Pembangunan)</h2>
			<ul class="space-y-2">
				<li><strong>Ramadhan 245 H / 859 M</strong>: Fatima membeli sebidang tanah kebun strategis di Fez.</li>
				<li><strong>Material halal</strong>: ia memastikan tanah dan seluruh bahan bangunan bersih dari syubhat.</li>
				<li><strong>Sumur sendiri</strong>: air untuk adukan semen, tanah liat, dan batuan diambil dari sumur dalam kompleks, menjaga kesucian dan keberkahan.</li>
			</ul>

			<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">3. Tirakat Puasa Selama Pembangunan</h2>
			<p>
				Selama pembangunan yang berlangsung sekitar dua hingga tiga tahun, Fatima berpuasa setiap hari (shaum dahr). Ia bernazar tidak akan berbuka selain malam hari hingga masjid selesai dan bisa dipakai shalat syukur. Setiap batu disusun dengan doa dan tirakat.
			</p>

			<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">4. Dari Masjid Menjadi Universitas</h2>
			<ul class="space-y-2">
				<li><strong>Struktur awal</strong>: ruang shalat, halaman tengah (sahn) dengan air mancur wudhu, dan menara adzan.</li>
				<li><strong>Halaqah ilmu</strong>: ulama mengajar tafsir, hadis, dan fiqh di tiang-tiang masjid (sistem sorogan/bandongan ala pesantren).</li>
				<li><strong>Ekspansi disiplin</strong>: matematika, astronomi, kedokteran, hingga musik ikut diajarkan.</li>
				<li><strong>Perpustakaan langka</strong>: manuskrip-manuskrip klasik disimpan, menarik penuntut ilmu dari berbagai negeri.</li>
				<li><strong>Efek domino</strong>: aktivitas yang kian terstruktur menjadikan masjid ini universitas; bahkan Paus Sylvester II disebut belajar matematika di sini dan membawa angka Arab ke Eropa.</li>
			</ul>

			<h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">5. Warisan Abadi</h2>
			<p>
